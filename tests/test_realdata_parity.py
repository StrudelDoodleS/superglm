@NB2_SKIP
class TestNB2ThetaEstimation:
    """NB2 theta estimation on freMTPL2freq."""

    def test_theta_near_poisson(self):
        """mgcv gets theta=17541.  Our estimator should find theta > 100."""
        from superglm.distributions import NegativeBinomial

        df, y, offset = _load_nb2_data()
        m = SuperGLM(
            features=_features(),
            family=NegativeBinomial(theta=1.0),
            lambda1=0,
        )
        result = m.estimate_theta(df, y, offset=offset, theta_bounds=(0.1, 100000))
        assert result.theta_hat > 100, f"Theta {result.theta_hat:.1f} should be >100 (near-Poisson)"


