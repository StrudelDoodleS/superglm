NB2_SKIP = pytest.mark.skipif(
    not _nb2_data_available(),
    reason="tests/fixtures/mtpl2_freq_50k.csv.gz not found",
)


@NB2_SKIP
class TestNB2ExactREML:
    """Poisson fit_reml(discrete=False) vs mgcv nb() — primary comparison.

    mgcv estimates theta=17541 — near-Poisson.  Since NB2 → Poisson as
    theta → ∞, we fit Poisson with exact REML (deviance and EDF match
    mgcv NB2 to <0.1%).
    """

    @pytest.fixture(scope="class")
    def model(self):
        df, y, offset = _load_nb2_data()
        m = SuperGLM(features=_features(), family="poisson", lambda1=0)
        m.fit_reml(df, y, offset=offset, max_reml_iter=30)
        return m, df, y, offset

    def test_convergence(self, model):
        m, *_ = model
        assert m._reml_result.converged

    def test_deviance(self, model):
        """Deviance within 0.1% of mgcv."""
        m, *_ = model
        rel = abs(m.result.deviance - MGCV_NB2["deviance"]) / MGCV_NB2["deviance"]
        assert rel < 0.001, (
            f"Deviance {m.result.deviance:.1f} vs mgcv {MGCV_NB2['deviance']:.1f} "
            f"(rel diff {rel:.5f})"
        )

    def test_edf_total(self, model):
        """Total EDF within ±2 of mgcv."""
        m, *_ = model
        diff = abs(m.result.effective_df - MGCV_NB2["edf_total"])
        assert diff < 2.0, (
            f"EDF {m.result.effective_df:.1f} vs mgcv {MGCV_NB2['edf_total']:.1f} (diff {diff:.1f})"
        )

    def test_prediction_mean(self, model):
        """Mean prediction within 5% of mgcv."""
        m, df, *_ = model
        mu = m.predict(df)
        assert np.all(mu > 0)
        rel = abs(mu.mean() - MGCV_NB2["pred_mean"]) / MGCV_NB2["pred_mean"]
        assert rel < 0.05, (
            f"pred_mean {mu.mean():.4f} vs mgcv {MGCV_NB2['pred_mean']:.4f} (rel diff {rel:.4f})"
        )

    def test_prediction_correlation_with_y(self, model):
        m, df, y, *_ = model
        mu = m.predict(df)
        r = np.corrcoef(y, mu)[0, 1]
        assert r > 0.30, f"pred-response correlation {r:.3f} too low"

    def test_prediction_vs_mgcv_fitted(self, model):
        """Predictions should correlate highly with mgcv fitted values."""
        m, df, *_ = model
        fitted_path = os.path.join(FIXTURES, "mgcv_nb2_mtpl2_fitted.csv.gz")
        if not os.path.exists(fitted_path):
            pytest.skip("mgcv fitted values not exported")
        mgcv_fitted = pd.read_csv(fitted_path)
        mu = m.predict(df)
        r = np.corrcoef(mu, mgcv_fitted["fitted"].values)[0, 1]
        assert r > 0.95, f"SuperGLM-mgcv pred corr {r:.4f}"


@NB2_SKIP
class TestNB2DiscreteREML:
    """Poisson fit_reml(discrete=True) — fREML with binned basis.

    Compared against exact REML (not mgcv directly).  Discretization
    should produce nearly identical results on 50k rows with 256 bins.
    """

    @pytest.fixture(scope="class")
    def models(self):
        df, y, offset = _load_nb2_data()
        # Exact REML baseline
        m_exact = SuperGLM(features=_features(), family="poisson", lambda1=0)
        m_exact.fit_reml(df, y, offset=offset, max_reml_iter=30)
        # Discrete fREML
        m_disc = SuperGLM(
            features=_features(discrete=True),
            family="poisson",
            lambda1=0,
            discrete=True,
            n_bins=256,
        )
        m_disc.fit_reml(df, y, offset=offset, max_reml_iter=30)
        return m_exact, m_disc, df, y

    def test_convergence(self, models):
        _, m_disc, *_ = models
        assert m_disc._reml_result.converged

    def test_deviance_vs_exact(self, models):
        """Discrete deviance within 0.5% of exact REML."""
        m_exact, m_disc, *_ = models
        rel = abs(m_disc.result.deviance - m_exact.result.deviance) / m_exact.result.deviance
        assert rel < 0.005, (
            f"Discrete dev {m_disc.result.deviance:.1f} vs exact {m_exact.result.deviance:.1f} "
            f"(rel diff {rel:.5f})"
        )

    def test_edf_vs_exact(self, models):
        """Discrete EDF within ±2 of exact REML."""
        m_exact, m_disc, *_ = models
        diff = abs(m_disc.result.effective_df - m_exact.result.effective_df)
        assert diff < 2.0, (
            f"Discrete EDF {m_disc.result.effective_df:.1f} vs exact {m_exact.result.effective_df:.1f} "
            f"(diff {diff:.1f})"
        )

    def test_prediction_correlation(self, models):
        """Discrete predictions should correlate >0.999 with exact."""
        m_exact, m_disc, df, *_ = models
        mu_exact = m_exact.predict(df)
        mu_disc = m_disc.predict(df)
        r = np.corrcoef(mu_exact, mu_disc)[0, 1]
        assert r > 0.999, f"exact-discrete pred corr {r:.6f}"


