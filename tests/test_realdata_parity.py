"""Real-data mgcv parity tests on freMTPL2.

Compares SuperGLM across all fitting paths against reference values from
mgcv::gam() (method="REML") on prepared subsets of freMTPL2freq/sev.

Fitting paths tested:
  1. fit_reml(discrete=False) — exact REML (primary mgcv comparison)
  2. fit_reml(discrete=True)  — fREML with binned basis
  3. fit(lambda1=0)           — direct IRLS, no REML
  4. fit(lambda1>0)           — group lasso + PIRLS BCD

Reference values generated by:
  scratch/r_experiments/realdata_parity_reference.R
  R 4.5.2, mgcv 1.9-3, bs="cr" (cubic regression spline)

Fixture data (committed as .csv.gz, CI-runnable):
  tests/fixtures/mtpl2_freq_50k.csv.gz   — 50k-row subset, sorted by IDpol
  tests/fixtures/mtpl2_sev_merged.csv.gz — freq+sev joined (~25k rows)
  Generated by scratch/export_parity_fixtures.py.

  mgcv fitted values (optional, for prediction comparison):
  tests/fixtures/mgcv_nb2_mtpl2_fitted.csv.gz
  tests/fixtures/mgcv_gamma_mtpl2sev_fitted.csv.gz
  Generated by scratch/r_experiments/realdata_parity_reference.R.
  Tests skip gracefully if these are absent.

Basis alignment:
  mgcv bs="cr" with k=K gives K-1 free basis columns after absorbing the
  identifiability constraint.  SuperGLM Spline(kind="cr", k=K) produces
  K-1 built columns (identifiability direction physically removed).
  The integrated-f'' penalty matches mgcv's default m=2 for cr.
"""

import os

import numpy as np
import pandas as pd
import pytest

from superglm import SuperGLM
from superglm.features.categorical import Categorical
from superglm.features.spline import Spline

FIXTURES = os.path.join(os.path.dirname(__file__), "fixtures")

# ── mgcv reference values ────────────────────────────────────────────
# R 4.5.2, mgcv 1.9-3, bs="cr", method="REML"

MGCV_NB2 = {
    # mgcv nb() with outer theta estimation.  theta=17541 means
    # near-Poisson (V(mu) ≈ mu).  At large theta, NB2 deviance
    # converges to Poisson deviance.
    "theta": 17541.2,
    "deviance": 39679.83,
    "edf_smooth": {"DrivAge": 7.00, "VehAge": 8.89, "BonusMalus": 6.91},
    "edf_total": 28.80,
    "intercept": -0.9837,
    "pred_mean": 0.5341,
    "pred_cor_y": 0.382,
}

MGCV_GAMMA = {
    "deviance": 39083.21,
    # mgcv "scale" is Pearson-based: sum((y-mu)^2/V(mu)) / (n-edf).
    # This differs from deviance-based phi = deviance/(n-edf).
    # For Gamma, deviance phi ≈ 1.57 while Pearson phi ≈ 22.47.
    "pearson_scale": 22.47,
    "edf_smooth": {"DrivAge": 13.63, "VehAge": 8.63, "BonusMalus": 8.62},
    "edf_total": 36.88,
    "intercept": 7.792,
    "pred_mean": 2333.6,
}


def _nb2_data_available():
    return os.path.exists(os.path.join(FIXTURES, "mtpl2_freq_50k.csv.gz"))


def _gamma_data_available():
    return os.path.exists(os.path.join(FIXTURES, "mtpl2_sev_merged.csv.gz"))


def _load_nb2_data():
    """Load prepared 50k NB2 subsample from committed fixture."""
    df = pd.read_csv(os.path.join(FIXTURES, "mtpl2_freq_50k.csv.gz"))
    y = df["ClaimNb"].values.astype(float)
    offset = np.log(df["Exposure"].values)
    return df, y, offset


def _load_gamma_data():
    """Load prepared Gamma severity data from committed fixture."""
    df = pd.read_csv(os.path.join(FIXTURES, "mtpl2_sev_merged.csv.gz"))
    y = df["ClaimAmount"].values.astype(float)
    return df, y


def _features(discrete=False):
    return {
        "DrivAge": Spline(kind="cr", k=15, discrete=discrete),
        "VehAge": Spline(kind="cr", k=10, discrete=discrete),
        "BonusMalus": Spline(kind="cr", k=10, discrete=discrete),
        "Area": Categorical(),
    }


